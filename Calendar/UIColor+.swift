import Foundation
import UIKit

extension UIColor {
    
    class func lightBlue() -> UIColor {
        return #colorLiteral(red: 0.3607843137, green: 0.7529411765, blue: 0.8235294118, alpha: 1)
    }
    
    class func lightRed() -> UIColor {
        return #colorLiteral(red: 0.7647058824, green: 0.4823529412, blue: 0.6862745098, alpha: 1)
    }
}
